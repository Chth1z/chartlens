"""Contract tests for the evaluation profile fixture set.

These tests guard the precision baseline. If any of them fails it means
either the fixtures, the gold YAML, the bootstrap script, the
ConservativeLocalProvider, or the rule-driven extraction has drifted in a
way that would invalidate before/after comparisons in `docs/ROADMAP.md`.

The baseline JSON (`config/evaluation_profiles/baselines/mock_general.json`)
is regenerated by:

    python scripts/bootstrap-eval-fixtures.py --profile-id mock_general --baseline

It is meant to change only when a precision task explicitly improves the
metrics; in that case the change includes the new baseline and a
`docs/ROADMAP.md` outcome note.
"""
from __future__ import annotations

import json
import sys
from pathlib import Path

import pytest


ROOT = Path(__file__).resolve().parents[2]
BACKEND_ROOT = ROOT / "backend"
if str(BACKEND_ROOT) not in sys.path:
    sys.path.insert(0, str(BACKEND_ROOT))

from app.core.config_loader import load_evaluation_profile  # noqa: E402
from app.core.database import (  # noqa: E402
    CaseRecord,
    FieldResultRecord,
    ModelCallRecord,
    ProcessingEventRecord,
    ProcessingRunRecord,
    ReviewAuditRecord,
    SessionLocal,
    init_db,
)
from app.services.extraction_eval import run_extraction_evaluation_profile  # noqa: E402
from app.services.llm_provider.local_extraction import ConservativeLocalProvider  # noqa: E402
from app.services.pipeline import process_case  # noqa: E402


PROFILE_ID = "mock_general"
FIXTURES_DIR = ROOT / "config" / "evaluation_profiles" / "fixtures" / PROFILE_ID
BASELINE_PATH = ROOT / "config" / "evaluation_profiles" / "baselines" / f"{PROFILE_ID}.json"


init_db()


def _cleanup_case(db, case_id: str) -> None:
    db.query(ModelCallRecord).filter(ModelCallRecord.case_id == case_id).delete()
    db.query(ProcessingEventRecord).filter(ProcessingEventRecord.case_id == case_id).delete()
    db.query(ProcessingRunRecord).filter(ProcessingRunRecord.case_id == case_id).delete()
    db.query(ReviewAuditRecord).filter(ReviewAuditRecord.case_id == case_id).delete()
    db.query(FieldResultRecord).filter(FieldResultRecord.case_id == case_id).delete()
    db.query(CaseRecord).filter(CaseRecord.case_id == case_id).delete()
    db.commit()


def _bootstrap_case(db, case_id: str, fixture: Path) -> None:
    _cleanup_case(db, case_id)
    case = CaseRecord(
        case_id=case_id,
        filename=fixture.name,
        file_hash=case_id,
        file_path=str(fixture),
        status="queued",
    )
    db.add(case)
    db.commit()
    db.refresh(case)
    process_case(db, case, semantic_provider=ConservativeLocalProvider())


def test_every_gold_case_has_a_matching_fixture_file():
    profile = load_evaluation_profile(PROFILE_ID)
    assert profile.gold_cases, "mock_general profile must have gold cases for the precision baseline"
    missing: list[str] = []
    for gold in profile.gold_cases:
        if not (FIXTURES_DIR / f"{gold.case_id}.txt").exists():
            missing.append(gold.case_id)
    assert not missing, f"missing fixture files for: {missing}"


def test_every_fixture_file_has_a_matching_gold_case():
    profile = load_evaluation_profile(PROFILE_ID)
    gold_ids = {gold.case_id for gold in profile.gold_cases}
    orphan_fixtures = [
        path.stem for path in FIXTURES_DIR.glob("*.txt") if path.stem not in gold_ids
    ]
    assert not orphan_fixtures, (
        f"fixtures without gold entries leak into the precision baseline: {orphan_fixtures}"
    )


def test_baseline_file_is_present_and_well_formed():
    assert BASELINE_PATH.exists(), (
        "baseline file is missing. Regenerate with: "
        "python scripts/bootstrap-eval-fixtures.py --profile-id mock_general --baseline"
    )
    payload = json.loads(BASELINE_PATH.read_text(encoding="utf-8"))
    assert payload.get("schema_version") == "extraction-eval-v1"
    assert payload.get("profile", {}).get("profile_id") == PROFILE_ID
    summary = payload.get("summary", {})
    assert "accuracy" in summary
    assert "auto_accept_precision" in summary
    assert "evidence_coverage" in summary
    assert "unknown_misfill_rate" in summary
    # Hard-coded baseline floor: precision improvements may only raise these.
    assert summary["evidence_coverage"] == pytest.approx(1.0)
    assert summary["unknown_misfill_rate"] == pytest.approx(0.0)
    assert summary["auto_accept_precision"] == pytest.approx(1.0)
    # Accuracy floor: the rule-only path now scores 1.0 on the synthetic
    # fixtures after E1-005 closed the positive-history clause-boundary gap.
    # Keep the floor exact so a regression below this number fails loudly.
    # To raise the floor again, regenerate the baseline as part of a
    # precision task and update this assertion in the same commit.
    assert summary["accuracy"] == pytest.approx(1.0)


@pytest.mark.skipif(
    not BASELINE_PATH.exists(),
    reason="baseline file missing; run scripts/bootstrap-eval-fixtures.py --baseline first",
)
def test_bootstrap_then_eval_reproduces_baseline_metrics():
    """End-to-end: bootstrap fixtures, run the eval, compare to baseline.

    This is the precision contract: under ConservativeLocalProvider the
    rule-driven baseline must match the on-disk baseline JSON. Any code
    change that moves the metrics has to update the baseline and the
    AGENTS.md Precision Tasks evidence in the same commit.
    """
    profile = load_evaluation_profile(PROFILE_ID)
    db = SessionLocal()
    try:
        for gold in profile.gold_cases:
            _bootstrap_case(db, gold.case_id, FIXTURES_DIR / f"{gold.case_id}.txt")
        report = run_extraction_evaluation_profile(PROFILE_ID, db=db)
    finally:
        try:
            for gold in profile.gold_cases:
                _cleanup_case(db, gold.case_id)
        finally:
            db.close()

    baseline = json.loads(BASELINE_PATH.read_text(encoding="utf-8"))
    summary = report["summary"]
    expected = baseline["summary"]
    assert summary["total_fields"] == expected["total_fields"]
    assert summary["correct"] == expected["correct"]
    assert summary["accuracy"] == pytest.approx(expected["accuracy"])
    assert summary["auto_accept_precision"] == pytest.approx(expected["auto_accept_precision"])
    assert summary["evidence_coverage"] == pytest.approx(expected["evidence_coverage"])
    assert summary["unknown_misfill_rate"] == pytest.approx(expected["unknown_misfill_rate"])
    # Per-case correctness should also match exactly so a precision task can
    # diff the cases that flipped.
    expected_correct = {case["case_id"]: case["correct"] for case in baseline["cases"]}
    actual_correct = {case["case_id"]: case["correct"] for case in report["cases"]}
    assert actual_correct == expected_correct
